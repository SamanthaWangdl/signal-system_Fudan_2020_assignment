dt=0.01;
dw=0.01*2*pi;
w=-8*pi:dw:8*pi;
t=-1:dt:1;
f=(t>-0.5).*(t<0.5);
Fw=dt*exp(w'*t*(-j))*f';
figure
subplot(2,1,1);
plot(w,Fw);

xt=dw*exp(j*t'*w)*Fw/(2*pi);
subplot(2,1,2);
plot(t,xt);

